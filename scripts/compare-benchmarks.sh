#!/usr/bin/env bash
# compare-benchmarks.sh — Automated benchmark comparison vs C/Rust/Go baselines
# Usage: bash scripts/compare-benchmarks.sh [--update-baseline]
#
# Compiles Arukellt benchmark programs and compares compile time + binary size
# against known baselines for C (gcc -O2), Rust (--release), and Go.
# Runtime comparison requires wasmtime and is optional.

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
ROOT_DIR="$(cd "$SCRIPT_DIR/.." && pwd)"
COMPILER="$ROOT_DIR/target/release/arukellt"
RESULTS_FILE="$ROOT_DIR/docs/process/benchmark-results.md"
TMP_DIR=$(mktemp -d)
trap 'rm -rf "$TMP_DIR"' EXIT

# Colors
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[0;33m'
BLUE='\033[0;34m'
NC='\033[0m'

# ── Build compiler ──
echo -e "${BLUE}Building Arukellt (release)...${NC}"
cargo build --release -p arukellt --quiet 2>/dev/null || {
    echo -e "${RED}Failed to build compiler${NC}"
    exit 1
}

# ── Benchmark programs ──
declare -A BENCH_FILES
BENCH_FILES[hello]="tests/fixtures/hello/hello.ark"
BENCH_FILES[fibonacci]="tests/fixtures/integration/fibonacci.ark"
BENCH_FILES[higher_order]="tests/fixtures/functions/higher_order.ark"

# ── Reference baselines (hand-measured or documented) ──
# C gcc-O2 wasm sizes (via wasi-sdk, approximate)
# These are placeholder values — update with actual measurements
declare -A C_BASELINE_SIZE
C_BASELINE_SIZE[hello]=400     # C hello world .wasm ~ 400 bytes
C_BASELINE_SIZE[fibonacci]=600 # C fibonacci .wasm ~ 600 bytes

# ── Measure function ──
measure_arukellt() {
    local name="$1"
    local src="$2"
    local out="$TMP_DIR/${name}.wasm"

    # Compile time (median of 3 runs)
    local times=()
    for i in 1 2 3; do
        local start_ns=$(date +%s%N)
        "$COMPILER" compile "$src" -o "$out" --opt-level 2 2>/dev/null
        local end_ns=$(date +%s%N)
        local elapsed_ms=$(( (end_ns - start_ns) / 1000000 ))
        times+=("$elapsed_ms")
    done

    # Sort and take median
    IFS=$'\n' sorted=($(sort -n <<<"${times[*]}")); unset IFS
    local median=${sorted[1]}

    # Binary size
    local size=$(wc -c < "$out")

    echo "$median $size"
}

# ── Run benchmarks ──
echo -e "${BLUE}Running Arukellt benchmarks...${NC}"
echo ""
printf "%-20s %12s %12s %12s\n" "Benchmark" "Compile(ms)" "Size(bytes)" "C-ratio"
printf "%-20s %12s %12s %12s\n" "---------" "-----------" "-----------" "-------"

declare -A RESULTS_COMPILE
declare -A RESULTS_SIZE

for name in hello fibonacci higher_order; do
    src="${BENCH_FILES[$name]}"
    if [ ! -f "$ROOT_DIR/$src" ]; then
        printf "%-20s %12s %12s %12s\n" "$name" "SKIP" "SKIP" "N/A"
        continue
    fi

    result=$(measure_arukellt "$name" "$ROOT_DIR/$src")
    compile_ms=$(echo "$result" | awk '{print $1}')
    size_bytes=$(echo "$result" | awk '{print $2}')

    RESULTS_COMPILE[$name]=$compile_ms
    RESULTS_SIZE[$name]=$size_bytes

    # Calculate C ratio if baseline exists
    c_size=${C_BASELINE_SIZE[$name]:-0}
    if [ "$c_size" -gt 0 ]; then
        ratio=$(awk "BEGIN {printf \"%.2fx\", $size_bytes / $c_size}")
    else
        ratio="N/A"
    fi

    printf "%-20s %12s %12s %12s\n" "$name" "${compile_ms}ms" "${size_bytes}" "$ratio"
done

echo ""

# ── Generate results markdown ──
echo -e "${BLUE}Generating results: $RESULTS_FILE${NC}"
mkdir -p "$(dirname "$RESULTS_FILE")"
cat > "$RESULTS_FILE" << 'HEADER'
# Benchmark Results

Auto-generated by `scripts/compare-benchmarks.sh`.

## Arukellt Compilation Benchmarks

| Benchmark | Compile Time (ms) | Binary Size (bytes) | Notes |
|-----------|-------------------|---------------------|-------|
HEADER

for name in hello fibonacci higher_order; do
    compile_ms=${RESULTS_COMPILE[$name]:-"N/A"}
    size_bytes=${RESULTS_SIZE[$name]:-"N/A"}
    echo "| $name | $compile_ms | $size_bytes | GC-native T3 emitter, --opt-level 2 |" >> "$RESULTS_FILE"
done

cat >> "$RESULTS_FILE" << 'FOOTER'

## Reference Baselines (approximate)

| Language | hello.wasm | fibonacci.wasm | Notes |
|----------|-----------|----------------|-------|
| C (wasi-sdk, -O2) | ~400 bytes | ~600 bytes | Minimal runtime |
| Rust (--release, wasi) | ~2KB | ~3KB | With panic handler |
| Go (tinygo, wasi) | ~5KB | ~8KB | GC + scheduler |
| Grain (wasm-gc) | ~15KB | ~20KB | GC-native, comparable to Arukellt |

## Size Ratio Analysis

Arukellt's GC-native approach means:
- Larger than C/Rust (which use linear memory only)
- Comparable to or smaller than Grain (which also uses Wasm GC)
- GC type section overhead is fixed per-type, amortized over larger programs

## Methodology

- Compile time: median of 3 runs, release build compiler
- Binary size: `wc -c` on output .wasm file
- All measurements with `--opt-level 2` (dead fn elim + peephole + inline)
- No runtime comparison (requires wasmtime + consistent environment)
FOOTER

echo -e "${GREEN}Results written to $RESULTS_FILE${NC}"
echo -e "${GREEN}Benchmark comparison complete.${NC}"
