def done(iid: str) -> bool:
    p = f"{int(iid):03d}-"
    if any(x.name.startswith(p) for x in DONE.glob("*.md")): return True
    return not any(x.name.startswith(p) for x in OPEN.glob("*.md"))

def dispatch(iid: str) -> str:
    if done(iid): return "done"
    if iid in {"118","076","637"} and not done("074"): return "blocked"
    if iid in {"183","638"} and not done("184"): return "blocked"
    return "ready"

def main() -> None:
    oc, dc = len(list(OPEN.glob("[0-9]*.md"))), len(list(DONE.glob("[0-9]*.md")))
    ts = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")
    lines = ["# Issue dispatch flows (independent lanes)", "", f"> Generated by `scripts/gen/generate-dispatch-flows.py` at **{ts}**.", "", f"> Queue: **{oc}** open / **{dc}** done.", "", "各レーンは **他レーンと依存しない**。レーン内は A→B→C の順で進める。", "", "## Multi-step lanes", "", "| Lane | Track | 現在地 | Step | ID | Priority | Dispatch | 概要 |", "|------|-------|--------|------|-----|----------|----------|------|"]
    for lane, track, pos, steps in MULTI:
        for step, iid, summary in steps:
            d = dispatch(iid)
            lines.append(f"| {lane} | {track} | {pos if not all(done(x) for _,x,_ in steps) else '完了'} | {step} | #{iid} | — | {d} | {summary} |")
    lines += ["","## Single-step lanes (A only)","","| Lane | Track | ID | Priority | Dispatch | 概要 |","|------|-------|-----|----------|----------|------|"]
    for lane, track, iid, summary in SINGLE:
        if done(iid): continue
        lines.append(f"| {lane} | {track} | #{iid} | — | {dispatch(iid)} | {summary} |")
    lines += ["", "## Regenerate", "", "```bash", "python3 scripts/gen/generate-issue-index.py", "python3 scripts/gen/generate-dispatch-flows.py", "```", ""]
    OUT.write_text("\n".join(lines), encoding="utf-8")
    print(f"Wrote {OUT.name} ({oc}/{dc})")
if __name__ == "__main__": main()
