#!/usr/bin/env bash
# scripts/generate-scoreboard.sh — Generate stdlib module maturity scoreboard
set -euo pipefail

REPO_ROOT="$(cd "$(dirname "$0")/../.." && pwd)"
OUTPUT="$REPO_ROOT/docs/stdlib/scoreboard.md"

python3 - "$REPO_ROOT" "$OUTPUT" << 'PYTHON_SCRIPT'
import toml, os, sys
from collections import Counter, defaultdict

repo_root = sys.argv[1]
output = sys.argv[2]

manifest_path = os.path.join(repo_root, "std/manifest.toml")
fixture_dir = os.path.join(repo_root, "tests/fixtures")

with open(manifest_path) as f:
    m = toml.load(f)

funcs = m.get("functions", [])
modules_meta = {mod["name"]: mod for mod in m.get("modules", [])}

# Group functions by family
families = defaultdict(list)
for fn in funcs:
    mod = fn.get("module", "prelude") or "prelude"
    parts = mod.split("::")
    if len(parts) >= 3:
        family = "::".join(parts[:3])
    elif len(parts) >= 2:
        family = "::".join(parts[:2])
    else:
        family = mod
    families[family].append(fn)

# Count fixtures per family by scanning for function name mentions
fixture_files = []
for root, dirs, files in os.walk(fixture_dir):
    for f in files:
        if f.endswith(".ark"):
            fixture_files.append(os.path.join(root, f))

fixture_content_cache = {}
def get_fixture_content():
    if not fixture_content_cache:
        for fp in fixture_files:
            try:
                with open(fp) as f:
                    fixture_content_cache[fp] = f.read()
            except:
                pass
    return fixture_content_cache

def count_fixture_hits(func_names):
    hits = 0
    contents = get_fixture_content()
    for name in func_names:
        for content in contents.values():
            if name in content:
                hits += 1
                break
    return hits

# Generate markdown
lines = []
lines.append("# Stdlib Module Maturity Scoreboard")
lines.append("")
lines.append("> Auto-generated by `scripts/generate-scoreboard.sh`.")
lines.append("> Do not edit manually.")
lines.append("")
lines.append("| Family | APIs | Stable | Exp | Depr | Fixture Coverage | Host Dep |")
lines.append("|--------|------|--------|-----|------|------------------|----------|")

total_apis = 0
total_stable = 0
total_exp = 0
total_depr = 0
total_covered = 0

for family in sorted(families):
    fns = families[family]
    total = len(fns)
    stable = sum(1 for fn in fns if not fn.get("deprecated_by") and fn.get("stability", "stable") != "experimental")
    exp = sum(1 for fn in fns if fn.get("stability") == "experimental" and not fn.get("deprecated_by"))
    depr = sum(1 for fn in fns if fn.get("deprecated_by"))
    host_dep = "yes" if any(fn.get("kind") in ("host_import", "host_stub") for fn in fns) else "no"
    
    func_names = [fn["name"] for fn in fns if not fn.get("deprecated_by")]
    covered = count_fixture_hits(func_names)
    active = total - depr
    coverage_pct = (covered * 100 // active) if active > 0 else 0
    
    total_apis += total
    total_stable += stable
    total_exp += exp
    total_depr += depr
    total_covered += covered
    
    lines.append(f"| `{family}` | {total} | {stable} | {exp} | {depr} | {covered}/{active} ({coverage_pct}%) | {host_dep} |")

lines.append("")
total_active = total_apis - total_depr
overall_pct = (total_covered * 100 // total_active) if total_active > 0 else 0
lines.append(f"**Total**: {total_apis} APIs, {total_stable} stable, {total_exp} experimental, {total_depr} deprecated, {total_covered}/{total_active} ({overall_pct}%) fixture coverage")
lines.append("")
lines.append("## Legend")
lines.append("")
lines.append("- **APIs**: Total function count in the family")
lines.append("- **Stable**: Functions with `stability = \"stable\"` (or default)")
lines.append("- **Exp**: Functions with `stability = \"experimental\"`")
lines.append("- **Depr**: Functions with `deprecated_by` set")
lines.append("- **Fixture Coverage**: How many non-deprecated functions appear in at least one test fixture")
lines.append("- **Host Dep**: Whether the family contains host-imported functions")
lines.append("")

with open(output, "w") as f:
    f.write("\n".join(lines))

print(f"Scoreboard written to {output}")
print(f"Total: {total_apis} APIs, {total_covered}/{total_active} ({overall_pct}%) coverage")
PYTHON_SCRIPT
